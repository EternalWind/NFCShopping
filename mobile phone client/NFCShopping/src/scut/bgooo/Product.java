package scut.bgooo;

/**
 * 商品信息实体类
 * */
public class Product {

	private int Id;
	private String Name;
	private int Rating;
	private float Price;

	public int getId() {
		return Id;
	}

	public String getName() {
		return Name;
	}

	public int getRating() {
		return Rating;
	}

	public float getPrice() {
		return Price;
	}
}
